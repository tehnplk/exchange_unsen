            latest_version_data = max(version_data_list, key=lambda x: x.get('version_code', 0))
            latest_version = latest_version_data.get('version_name', '1.0.0')
            
            # ‡πÄ‡∏õ‡∏£‡∏µ‡∏¢‡∏ö‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ä‡∏±‡∏ô
